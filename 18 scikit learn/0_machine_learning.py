# Il machine learning (apprendimento automatico) è un campo dell’intelligenza artificiale
# che si occupa di costruire algoritmi in grado di apprendere automaticamente dai dati,
# senza essere esplicitamente programmati per ogni singolo compito.
#
# In termini semplici, il machine learning permette a un sistema di:
#
#     Osservare dati,
#
#     Riconoscere pattern o schemi,
#
#     Prendere decisioni o fare previsioni su dati nuovi, basandosi su ciò che ha appreso.


# Tipologie principali di machine learning:
#
#     Apprendimento supervisionato
#     Il modello impara da dati etichettati (cioè ogni esempio ha una risposta corretta).
#
#         Esempi: classificazione (spam vs non spam), regressione (previsione dei prezzi).
#
#     Apprendimento non supervisionato
#     Il modello esplora dati non etichettati per trovare strutture nascoste.
#
#         Esempi: clustering (raggruppamento clienti), riduzione dimensionale.
#
#     Apprendimento per rinforzo
#     L’agente impara interagendo con un ambiente, ricevendo ricompense o penalità.
#
#         Esempi: giochi, robotica, guida autonoma.


# Esempi pratici:
#
#     Riconoscimento vocale (es. Siri, Alexa)
#     Raccomandazioni su Netflix o Amazon
#     Previsioni di borsa
#     Rilevamento di frodi finanziarie
#     Diagnosi mediche automatiche


# Differenze rispetto al deep learning:
#
#     Il machine learning classico usa algoritmi come decision tree, SVM, regressione lineare ecc.
#
#     Il deep learning è una sotto-categoria del machine learning che usa reti neurali profonde,
#     ed è particolarmente utile con grandi quantità di dati e alta complessità.