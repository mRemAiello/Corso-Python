# Il deep learning è un sottoinsieme del machine learning che si basa su reti neurali
# artificiali composte da molti strati (da cui il termine "deep", ovvero profondo).
# È una tecnica che consente ai computer di imparare dai dati in modo gerarchico,
# cioè costruendo progressivamente concetti più complessi a partire da quelli più semplici.

# Come funziona in sintesi:
#
#     Utilizza reti neurali artificiali multilivello (deep neural networks).
#     Ogni strato della rete elabora l'input in una forma sempre più astratta.
#     È particolarmente efficace nell'elaborazione di dati non strutturati: immagini, audio, video, testo.
#

# Esempi pratici:
#
#     Visione artificiale: riconoscimento facciale, classificazione di immagini.
#     Elaborazione del linguaggio naturale: traduzione automatica, chatbot, riconoscimento vocale.
#     Veicoli autonomi: interpretazione di segnali stradali, ostacoli, traiettorie.
#     Medicina: diagnosi automatica da immagini (es. radiografie).
#

# Vantaggi:
#
#     Non richiede feature engineering manuale (estrazione manuale delle caratteristiche).
#     Può raggiungere performance superiori a quelle umane in alcuni compiti (es. diagnosi, riconoscimento immagini).
#

# Svantaggi:
#
#     Richiede molti dati e potenza di calcolo elevata.
#     È spesso percepito come una “scatola nera” (difficile da interpretare).